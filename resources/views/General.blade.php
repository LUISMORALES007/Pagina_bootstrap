                           <h2 align="center"><a name="contenido4"></a> QUE ES UNA ARBOL? </h2>


                        <h4 align="center">
                            un árbol es un tipo abstracto de datos (TAD) ampliamente usado que imita la estructura jerárquica de un árbol, con un valor en la raíz y subárboles </br>con un nodo padre, representado como un conjunto de nodos enlazados.</br>

                            Una estructura de datos de árbol se puede definir de forma recursiva (localmente) como una colección de nodos (a partir de un nodo raíz), donde</br> cada  nodo es una estructura de datos con un valor, junto con una lista de referencias a los nodos (los hijos) , con la condición de que ninguna </br>referencia esté duplicada ni que ningún nodo apunte a la raíz.</br>

                            Alternativamente, un árbol se puede definir de manera abstracta en su conjunto como un árbol ordenado, con un valor asignado a cada nodo. Ambas</br> perspectivas son útiles: mientras que un árbol puede ser analizado matemáticamente, realmente es representado como una estructura de datos en la que</br> se trabaja con cada nodo por separado (en lugar de como una lista de nodos y una lista de adyacencia entre nodos, como un grafo). Mirando a un</br> árbol como conjunto, se puede hablar de el nodo padre de un nodo dado, pero en general se habla de una estructura de datos de un nodo dado que </br>sólo contiene la lista de sus hijos sin referencia a su padre (si lo hay).</br></br></br></br>
                            
                             
