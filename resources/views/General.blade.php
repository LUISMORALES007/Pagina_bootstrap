                        <h2 align="center"><a name ="contenido1"></a> QUE SON LAS ESTRUCTURAS DE INFORMACION? </h2>
                    
                        

                        <h4 align="center"> 
                            es una forma particular de organizar datos en una computadora para que pueda ser utilizado de manera eficiente. Diferentes tipos de estructuras</br> de datos son adecuados para diferentes tipos de aplicaciones, y algunos son altamente especializados para tareas específicas.</br>

                           Las estructuras de datos son un medio para manejar grandes cantidades de datos de manera eficiente para usos tales como grandes bases de datos y </br>servicios de indización de Internet. Por lo general, las estructuras de datos eficientes son clave para diseñar algoritmos eficientes. Algunos métodos </br>formales de diseño y lenguajes de programación destacan las estructuras de datos, en lugar de los algoritmos, como el factor clave de</br> organización en el diseño de software</br>


