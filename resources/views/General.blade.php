                        <h2 align="center"><a name="contenido2"></a> QUE ES UNA PILA ?</h2>


                        <h4 align="center">
                                      

                                  Una pila (stack en inglés) es una lista ordenada o estructura de datos que permite almacenar y recuperar datos, el modo de acceso a sus </br>elementos es de tipo LIFO (del inglés Last In, First Out, «último en entrar, primero en salir») . Esta estructura se aplica en multitud de </br>supuestos en el área de informática debido a su simplicidad y capacidad de dar respuesta a numerosos procesos.</br>

                                  Para el manejo de los datos cuenta con dos operaciones básicas: apilar (push), que coloca un objeto en la pila, </br>y su operación inversa,retirar (o desapilar, pop), que retira el último elemento apilado.

                                 En cada momento sólo se tiene acceso a la parte superior de la pila,</br> es decir, al último objeto apilado (denominado TOS,</br> Top of Stack en inglés). La operación retirar permite la obtención de este elemento, que es retirado de la pila permitiendo el acceso al anterior (apilado</br> con anterioridad), que pasa a ser el último, el nuevo TOS.</br>

                                      Las pilas suelen emplearse en los siguientes contextos:</br>
                                      </br></br>
                                      Evaluación de expresiones en notación postfija (notación polaca inversa).</br>
                                      Reconocedores sintácticos de lenguajes independientes del contexto</br>
                                      Implementación de recursividad.</br>
                                     
