                                <h2 align="center"><a name="contenido3"></a> QUE ES UNA COLA ?</h2>


                        <h4 align="center">
                            
                                Una cola (también llamada fila) es una estructura de datos, caracterizada por ser una secuencia de elementos en la que la operación de inserción</br> push se realiza por un extremo y la operación de extracción pop por el otro. También se le llama estructura FIFO (del inglés First In </br>First Out), debido a que el primer elemento en entrar será también el primero en salir.</br></br>

                                Las colas se utilizan en sistemas informáticos, transportes y operaciones de investigación (entre otros), dónde los objetos, personas o eventos</br> son tomados como datos que se almacenan y se guardan mediante colas para su posterior procesamiento. Este tipo de estructura de datos </br>abstracta se implementa en lenguajes orientados a objetos mediante clases, en forma de listas enlazadas.</br></br></br>


